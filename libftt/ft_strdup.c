#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		n;
	char	*dup;
	int		i;

	i = 0;
	n = 0;
	while (src[n])
		n++;
	dup = (char *)malloc(sizeof(char) * (n + 1));
	if (!dup)
		return (NULL);
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
